            <h2>E-bike Speed Classifications</h2>
            <p>
                <span>
                    How fast an e-bike can go is directly related to its classification, which is based on things like
                    motor power and the amount of pedal help it offers. It's important to know these differences in
                    order to choose an e-bike that fits your needs and the rules in your area.

                </span>
            </p>
            <h3>Class 1: Pedal-Assisted up to 20 mph</h3>
            <p>
                <span>
                    Class 1 e-bikes only use electricity to help the person when they are pedaling. Once the speed hits
                    20 mph, the motor turns off. These e-bikes are <a
                        href="https://newurtopia.com/blogs/blog/best-long-range-electric-bike"> great for taking long
                        rides,</a> commuting in cities, or
                    enjoying paved trails.
                </span>
            </p>
            <h3>Class 2: Throttle-Assisted up to 20 mph</h3>
            <p>
                <span>
                    Along with pedal help, Class 2 e-bikes have a throttle that lets the rider go up to 20 mph without
                    pedaling. Because they are easier to use, they are great for riders who need a break from pedaling
                    or who have trouble moving around.

                </span>
            </p>
            <h3>Class 3: Pedal-Assisted up to 28 mph
            </h3>
            <p>
                <span>
                    With Class 3 e-bikes, the pedal help is stronger, and riders can go as fast as 28 mph. These
                    <a href="https://newurtopia.com/products/urtopia-carbon-fusion-gt">powerful e-bikes</a> are great
                    for longer commutes, riding up steep hills, or exploring rougher
                    territory.
                </span>
            </p>
